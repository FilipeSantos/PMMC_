                <h3>Paraolimpíada</h3>

                <p>Dirceu morava com a irmã, Rosângela de Moraes, e com o cunhado, Wanderlei José de Moraes. A neta do cunhado, Juliana, o ajudava a treinar em tempo integral, além dos trabalhos com a técnica Ana Carolina Lemos Alves. Ao lado disso, passou a estudar os adversários, principalmente Leong Yuki Wing, de Hong Kong. “Até sonhava que estava jogando com ele”, brinca.</p><br>
                
                <p>Na China, uma cena marcou a cabeça do futuro medalhista: “Fomos ao Estádio Olímpico ver a Maurren Maggi saltar. Quando ela cravou a marca que deu a medalha de ouro no salto em distância, quase chorei. Era uma história parecida com a minha, já que ela tinha ficado parada por doping”. Na Paraolimpíada, Dirceu venceu a estreia e perdeu o segundo jogo. No terceiro, uma vitória apertada e ele ficou em segundo lugar na chave, empatado em todos os quesitos com um competidor português, exatamente o que havia ganhado dele. Foram para um jogo extra e Dirceu ganhou. Após passar pelas oitavas e pelas quartas-de-final, ocorreu o encontro com o companheiro Eliseu, na semifinal olímpica. Vitória e classificação para a final, justamente contra Leong Yuki Wing.</p><br>

				<h3>A conquista</h3>
    
                <p>O jogo foi nervoso e sofrido até o último momento. No lance decisivo, o adversário precisava fazer três pontos para ganhar. “Quando ele iria jogar a bola, abaixei a cabeça, fechei os olhos e fiquei esperando. Quando os brasileiros começaram a gritar, desabei. Lembrei de 2005, quando eu estava inelegível, não era nada. Eu era um deficiente, um cadeirante e olha onde Deus havia me colocado”. Dirceu foi ao pódio ao lado de Eliseu, que conquistara a medalha de bronze. “O momento mais emocionante foi quando o locutor anunciou o hino nacional. Pensei que era o sonho realizado. É uma emoção inexplicável”, relembra. Nos dias seguintes, as disputas das duplas e nova medalha de ouro, fato inédito na história olímpica.</p><br>
                
                <p>A volta ao Brasil teve recepção especial no aeroporto de Cumbica e desfile em caminhão do Corpo de Bombeiros em Mogi das Cruzes. “Um deficiente mogiano e brasileiro, sem perspectiva de vida, conquistou o mundo. Às vezes, as pessoas com deficiência ficam em casa, a gente mesmo faz a nossa discriminação”, ensina Dirceu, que é o primeiro colocado no ranking mundial da categoria.</p><br>
                
                <h3>Futuro</h3>
                
                <p>No ano passado, Dirceu conquistou o título mundial, em Lisboa. Sua vaga para as Paraolímpíadas de Londres está garantida. Mas no caminho até a capital inglesa ainda tem a Copa do Mundo, em agosto, na Irlanda. “Hoje, estou em um nível mais avançado que em 2008 e mais experiente. Existem boas chances de repetir o feito”. Alguém duvida?</p><br>

            
            
            
            
            
            
            
