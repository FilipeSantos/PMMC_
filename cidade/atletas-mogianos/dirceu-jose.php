                <h2 class="subtitle">Dirceu José: atleta de ouro de Mogi e do Brasil</h2>
                <span>Conheça a comovente história do nosso campeão paraolímpico </span>
                <br><br>
                <p>Um sonho distante que muitas vezes pareceu impossível e que, ao ser realizado, se tornou maior do que havia sonhado. A trajetória do jogador de bocha adaptada, Dirceu José Pinto, é repleta de obstáculos, mas foi coroada com duas medalhas de ouro na Paraolimpíada de Pequim, em 2008. Conquistas inéditas para a modalidade no país.</p><br>
                
                <p>“Na verdade, eu só tenho a dimensão da conquista quando as pessoas vêm falar comigo sobre isso. Quando fui para Pequim, a meta era ganhar uma medalha de bronze com o Eliseu Santos, do Paraná, para divulgar a bocha no Brasil e dar oportunidade para que outras pessoas com deficiência pudessem sair de casa e praticar. Conseguimos muito mais que isso”, diz Dirceu. Hoje, ele compete pela equipe formada por uma parceria entre o Trabalho de Apoio ao Deficiente (Tradef) e a Secretaria Municipal de Esporte e Lazer de Mogi das Cruzes, além de ser atleta do Comitê Paraolímpico Brasileiro e coordenador do Paradesporto da Prefeitura de Mogi das Cruzes.</p><br>
                
                <h3>Início e lições</h3>
                
                <p>A história de Dirceu na bocha adaptada começou em 2002, quando ele descobriu a extensão de uma doença degenerativa que destrói, pouco a pouco, os tecidos musculares. Antes disso, o paulista de Francisco Morato, que mudou-se para Mogi das Cruzes aos dez anos, praticava natação.</p><br>
                
                <p>“Em 2002, quando eu terminei os estudos, minha vida seria de casa para o tratamento e vice-versa. Um dia, o professor que treinava bocha no Clube Náutico me chamou para fazer um teste. Eu não sabia o que era aquilo”, relembra.</p><br>
                
                <p>O professor era Ronaldo Tadeu, que trabalhava com o Tradef. “Eu estava saindo de uma vida de andante para uma de cadeirante por causa da doença. Isso me abalou psicologicamente. Comecei a praticar e a minha vida mudou completamente. Conheci pessoas com deficiência que levavam uma vida normal”, afirma. A primeira competição internacional foi o Parapan-Americano de 2003, em Mar Del Plata, na Argentina. “Nunca perdi tanto na vida, e ainda mais para argentinos. Fui vice-campeão brasileiro em 2003 e logo depois convocado para a Seleção Brasileira. Aquilo me subiu à cabeça. Aprendi muito a ter humildade, a respeitar o próximo”, explica.</p><br>
                
                <h3>Queda</h3>
                
                <p>Após conquistar o bicampeonato brasileiro em 2004 e 2005, a meta passou a ser a Olimpíada de 2008, em Pequim. A primeira chance para realizar o sonho era a Copa América de 2005. Só que Dirceu foi chamado para uma reclassificação, procedimento para definir o grau de comprometimento do paratleta. Os organizadores acreditavam que ele tinha força superior à permitida para jogar bocha.</p><br>
                <p>“Fui tornado inelegível. Isso quer dizer que o atleta fica dois anos sem jogar e, no terceiro ano, vai para uma reclassificação. Se não for aprovado, nunca mais poderá jogar. Era o sonho de ir para a Paraolimpíada que eu tinha indo embora”, lembra.</p><br>
                <p>Sem poder jogar, ele tomou uma decisão drástica, a de parar com os tratamentos de natação, hidroterapia e fisioterapia para não retardar mais os sintomas da doença. Com isso, em março de 2006, ele já não andava. Dirceu parou com os treinamentos e se aprofundou no estudo da Bíblia, outra paixão. </p><br>
                
                <h3>A volta</h3>
                
                <p>A volta às competições aconteceu no Campeonato Regional, em 2007. Logo depois, o momento decisivo: a Copa do Mundo realizada em Montreal, no Canadá, quando ocorreria também a nova reclassificação para competições internacionais. Ele foi analisado por quatro examinadores.</p><br>
                <p>“Estava três a um para me cortar. Lembrei de uma biopsia que eu havia feito e que dizia que eu tinha distrofia muscular cintural. Assim, duas examinadoras mudaram de ideia e ficou três a um para mim. Só uma portuguesa que não arredava pé, mas acabou concordando que eu ficasse em observação. Eu explodi de felicidade”, disse.</p><br>
                
                <p>Na competição, Dirceu foi eliminado nas oitavas de final. Nas duplas, nova derrota nas oitavas. O resultado garantiu a classificação de dois atletas brasileiros para a Paraolimpíada de Pequim: Dirceu e Eliseu.</p><br>
                
