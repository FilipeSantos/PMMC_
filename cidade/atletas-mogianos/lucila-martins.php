                <h2 class="subtitle">De colecionadora de títulos à professora de nadadores campeões.</h2>
                <span>Conheça Lucila, a mogiana que conquistou as piscinas da América Latina</span>
                <br><br>
                <p>Em casa, são exatas 1.050 medalhas guardadas, frutos da participação em competições desde o início da década de 1960. Ao lado delas, mais uma incontável coleção de troféus e homenagens. Lucila Martins alcançou títulos em piscinas do Brasil e do exterior e ainda hoje luta por conquistas dentro d’água.</p><br>

Na lista de conquistas estão a medalha de bronze no revezamento 4 x 100 metros livres nos Jogos Pan-Americanos de 1971, em Cali (COL), e seis medalhas obtidas em três edições de Campeonatos Sul-Americanos.</p><br>
                
                <p>A natação é a minha vida. Depois que parei de nadar, fiquei 25 anos fora e quando voltei pensei que não sabia como eu tinha ficado tanto tempo fora. A natação é o que eu sei e o que gosto", afirma a ex-nadadora, que também se tornou uma referência no ensino da modalidade em Mogi das Cruzes.</p><br>
				<br>
				<h3>Início</h3>
                
                <p>A história de Lucila Martins na natação começou fora das piscinas. Por influência do pai, Luiz Martins, ela aprendeu a nadar no rio Tietê, em uma época em que o curso d’água era uma das opções de lazer para os mogianos. Luiz já era nadador e competia em travessias de rio. "Nós somos em seis irmãos e ele ensinou um a um. Eu gostei, mas só tinha o rio para praticar. Um pouco depois, o Clube Náutico construiu a piscina e fomos treinar lá. Eu não pensava em competir; entrei para aprender. Não tinha na cabeça ser atleta", lembra.</p><br>
                
                <p>Apesar de não estarem nos planos da nadadora, as competições não demoraram a aparecer. A primeira foi em um torneio amistoso entre o clube e uma empresa da Zona Leste de São Paulo. Lucila ficou em segundo lugar, já nadando o que seria a sua especialidade: o nado de costas.</p><br>
                
                <p>No começo, as competições amistosas eram os principais eventos para a equipe de natação, em disputas principalmente contra equipes do Vale do Paraíba e da Capital. A mudança no foco e a chegada em torneios de ponta vieram a partir do Campeonato Paulista de 1967. "Eu não sabia que uma competição levava para outra, não tinha ideia do que era índice. Não ligava para isso. Eu só sabia que tinha de treinar bastante, sempre", diz.</p><br>
                
                <br>
                <h3>Campeonatos Sul-Americanos</h3>
                
                <p>No Paulista, veio a classificação para o Troféu Brasil daquele ano e a oportunidade para tentar uma vaga nos Jogos Pan-Americanos de 1967, que aconteceu em Winnipeg, no Canadá. </p><br>
                <p>"O Troféu Brasil aconteceu no Rio de Janeiro e foi uma eliminatória para o Pan. Não consegui o índice, mas várias pessoas de São Paulo foram para a competição. Na volta, eles me contaram como foi, que tinha sido muito bom. Foi aí que comecei a treinar mais forte para tentar ir ao próximo", lembra.</p><br>
                
                <p>No ano seguinte, veio a primeira competição internacional, o Campeonato Sul-Americano, realizado no Rio de Janeiro. Os resultados conseguidos nas piscinas cariocas provaram que Lucila era uma das principais nadadoras brasileiras da época. Ela fez parte da equipe campeã do revezamento 4 x 100 metros livres, foi medalha de prata nos 200 metros medley e bronze nos 100 metros nado de costas.</p><br>
                
                <p>"Mas o que mais me marcou foram as amizades com as atletas de outros países. Conheci muitas pessoas com quem mantive contato depois, por carta. Foi assim até o Sul-Americano de 1970, que foi no Peru, quando tive a oportunidade de encontrá-las de novo e fazer outras amizades", diz.</p><br>
                
                No Sul-Americano do Peru, Lucila conquistou mais uma medalha de bronze na prova dos 100 metros costas. Como em 1968, no entanto, as lembranças vão além das piscinas. "Eu nunca havia viajado de avião, nem conhecido outro país. Então, esta foi mais uma experiência de vida que a natação me proporcionou", conta.</p><br>

                <br>
                <h3>Jogos Pan-Americanos</h3>
                
                <p>O grande momento na carreira, no entanto, viria no ano seguinte, nos Jogos Pan-Americanos disputados em Cali, na Colômbia. Nos 100 metros costas, ela conquistou o quinto lugar. Mas uma nova chance de medalha apareceu com o revezamento 4 x 100 metros livres.</p><br>
                
                <p>"Eu também nadava crawl, e o Fernando Soraggi, que era o meu técnico, pediu uma eliminatória para a definição da equipe. A comissão técnica da Seleção Brasileira aceitou e eu consegui ficar entre as melhores. Assim, eu fui para a disputa do revezamento", diz.</p><br>
                
                <p>Além de Lucila, a equipe brasileira foi formada por Rosimere Prado, Maria Tereza e Lucy Burle. "Falaram para a gente que nós tínhamos de pegar o terceiro lugar porque Estados Unidos e Canadá já tinham, pelo nível das atletas, o primeiro e o segundo lugares garantidos. Nós sabíamos que tínhamos potencial para isso, mas entramos um pouco nervosas já que nunca ninguém está livre de um erro na virada ou no revezamento, o que poderia causar a desclassificação da equipe. Mas, tudo deu certo e conseguimos alcançar a meta", lembra. O tempo da equipe brasileira na prova foi de 5min14s.</p><br>
                
                <p>O bom desempenho nas competições levou Lucila para o Campeonato Mundial de 1972, na Iugoslávia. Ela disputou os 100 metros nado de costas, mas não chegou à final da competição. </p><br>
				<br>
                <h3>Mais conquistas</h3>
                
                <p>As conquistas internacionais voltaram em 1972 com as medalhas de prata nos 200 metros nado medley e de bronze nos 100 metros nado de costas, no Campeonato Sul-Americano do Chile. No mesmo ano, ela também conquistou medalha no Campeonato Luso-Brasileiro, organizado para comemorar os 150 anos da Independência do Brasil.</p><br>
                
                <p>"Todas essas conquistas foram importantes e compensadoras. Mas o que mais me marcou foi eu ter sido seis vezes campeã paulista nos 100 metros costas. Era uma emoção sair da piscina e ouvir meu nome como vencedora e recordista paulista", lembra Lucila, que conquistou o título paulista entre os anos de 1968 e 1973.</p><br>
                
                <br>
                <h3>Professora</h3>
                
                <p>A carreira como nadadora terminou em 1974. A opção de Lucila foi privilegiar a vida pessoal e a profissão como professora de Educação Física. Ela ficou 25 anos sem cair em uma piscina para disputar uma prova. O retorno aconteceu em 1999.</p><br>
                
                <p>"Eu casei, tive filhos, fui dar aulas de natação e pensei que não tinha mais que nadar. Em 1999, aconteceu uma competição aqui em Mogi e eu fui convidada. Aí, eu me reencontrei com a natação. Hoje, nado no meu ritmo, dentro da minha idade, principalmente em travessias, que eu gosto bastante", pontua.</p><br>
                
                <p>O período longe das piscinas, no entanto, trouxe outra grande recompensa para a ex-nadadora. "Minha gratificação foi poder passar para frente tudo aquilo que eu aprendi. Dei aula de natação por mais de 20 anos e hoje vejo professores que foram meus alunos, atletas que estão aí competindo que aprenderam a nadar comigo. Isso traz um sentimento muito bom", finaliza.</p><br>
