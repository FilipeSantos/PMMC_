                <h2 class="subtitle">Akcel de Godoy: orgulho mogiano nas piscinas.</h2>
                <span>Mergulhe na carreira deste atleta que foi bronze no Pan-Americano de 1975.</span>
                <br><br>
                <p>Os olhos brilham ao lembrar o mês de setembro de 1975. Na final do revezamento 4x100 metros medley dos Jogos Pan-Americanos do México, a Seleção Brasileira estava posicionada para a disputa contra alguns dos principais nadadores do mundo. Na baliza, Akcel de Godoy era um dos representantes do que o Brasil tinha de melhor na natação, em uma equipe que conquistaria a medalha de bronze da competição.</p><br>
                
                <p>“A equipe era formada pelo Rômulo Arantes Júnior, que nadava costas e depois foi ator, José Silvio Fiolo, no nado peito, Akcel de Godoy, no nado borboleta, e José Luciano Namorado, que fechava no estilo livre. Fizemos uma grande prova e só não chegamos à medalha de prata por 10 centésimos de segundo. Até hoje eu me arrepio em lembrar de subir ao pódio e ver a bandeira brasileira”, lembra o ex-nadador.</p><br>
                
                <h3>O início</h3>
                
                <p>A história de Akcel no esporte começou aos seis anos, quando a família mudou-se do Paraná para São José dos Campos. Lá, o pai, Honorato de Godoy, conheceu o professor Davi Camargo Machado, que encaminhou os três filhos para a natação, na Associação Esportiva São José.</p><br>
                
                <p>Pelo clube, Akcel foi campeão paulista infantil e participou do Campeonato Brasileiro Juvenil, realizado na piscina da Fonte Nova, em Salvador. As competições pela Associação Esportiva duraram até 1968, quando a modalidade acabou no clube e os nadadores passaram a treinar no Centro Técnico da Aeronáutica (CTA).</p><br>
                
                <p>“No inverno, não tinha como cair na água por causa da temperatura. Então, nós íamos treinar no tanque de resfriamento dos computadores do CTA, que tinha uns 15 metros de comprimento por 15 metros de largura. Como a água passava pelas máquinas, ela voltava aquecida. Assim, podíamos treinar”, recorda Akcel.</p><br>
                
                <h3>Mudança</h3>
                
                <p>Apesar dos treinamentos até com água quente, os nadadores não tinham como competir pelo CTA. “Meu pai, vendo isso, falou com o Davi, que já era meu padrinho de crisma, e acertou minha vinda para Mogi das Cruzes, em 1972. Viemos eu, meu irmão Ewerson e outros três atletas para nadar pelo Clube Náutico”, diz.</p><br>
                
                <p>No início, os treinos em Mogi aconteciam apenas nos finais de semana, já que Akcel e Ewerson tinham de terminar o ano letivo. Durante a semana, os irmãos seguiam um programa de treinamento ministrado pelo técnico Fernando Soraggi. A mudança definitiva para a cidade aconteceu em 1973, quando o jovem foi morar na casa de Olavo e Bete Secomandi durante seis meses, antes de se mudar para o alojamento do clube. “Foi a minha segunda família. Eles me acolheram com muito carinho e tenho os dois como pais para mim”, destaca.</p><br>
 
                O auge da carreira começou a chegar no início de 1975, com o título do Troféu Brasil de natação, na prova dos 100 metros borboleta. O resultado garantiu a classificação para os Jogos Pan-Americanos, que seriam realizados em agosto daquele ano, na Cidade do México.</p><br>
                
                <p>“A sensação de ser campeão é inexplicável. Você superar todos os grandes atletas graças a seu esforço, ao treinamento e conquistar uma vaga no Pan-Americano. Foi inesquecível”, lembra.</p><br>
                
                <h3>Jogos Pan-Americanos</h3>
                
                <p>A preparação para a competição foi dividida em treinamentos no Rio de Janeiro e na própria Cidade do México para adaptação aos mais de 2,2 mil metros de altitude do local. Mas não era só a “falta de ar” que preocupava o nadador.  “Lá, eu senti, pela primeira vez na vida, um tremor de terra. Foi algo muito rápido. Outra passagem curiosa aconteceu no alojamento. Como eu era calouro na Seleção, existia a tradição de receber um trote. Eu não queria ficar careca, então eu ia até o refeitório e pegava uma fruta, um iogurte, alguma coisa para comer e passava para o pessoal. Consegui me livrar”, diverte-se.</p><br>
                
                <p>O primeiro dia de provas foi de rodada tripla. Pela manhã, a prova eliminatória dos 100 metros borboleta. Como melhor brasileiro, Akcel conseguiu a classificação para a prova de revezamento 4x100 metros medley (que engloba os quatro estilos da natação). No entanto, na soma dos tempos das eliminatórias, o brasileiro ficou empatado com um competidor mexicano. “Nadamos um ao lado do outro e eu ganhei. Fui para a final, mas estava meio pregado. Consegui melhorar o meu tempo, mas fiquei em oitavo lugar e ainda fui selecionado para o exame antidoping”, conta.</p><br>
                
                <p>Logo depois, o grande dia do revezamento 4 x 100 metros medley. O primeiro lugar ficou com os Estados Unidos, que faturaram 27 das 29 medalhas de ouro em disputa nas piscinas mexicanas. O Brasil brigou até o final com o Canadá pelo segundo lugar, mas terminou com o bronze.</p><br>
                
                <p>“Representar o Brasil foi inexplicável porque passei por uma instabilidade tão grande, dúvidas e incertezas entre a saída da Associação Esportiva São José e a chegada ao Náutico, que quase parei de nadar. Quando veio a medalha, foi muito gratificante fazer parte desta elite”, afirma.</p><br>
                
                <p>Na volta ao Brasil, os Jogos Abertos, realizados em Pirassununga. “Eu vinha bem, com a oxigenação em alta e bati o recorde dos 200 metros borboleta, com o tempo de 2’10’’50. Esta marca permaneceu por dez anos”.</p><br>
                
                <h3>Viagens</h3>
                 
                <p>Em 1976, duas medalhas no Campeonato Sul-Americano, realizado em Maldonado, no Uruguai: a prata nos 100 metros borboleta e o ouro no revezamento 4 x 100 metros medley. Na mesma competição, Ewerson conquistaria a medalha de ouro nos 1.500 metros livres.</p><br>
                
                <p>“A Confederação Brasileira deu preferência para outro nadador, que era carioca. Então, eu fiz todo o aquecimento do meu irmão e fiquei controlando a prova para ele, informando quantas voltas faltavam. Em determinado momento, pessoas da Confederação Sul-Americana me tiraram de lá. Isso parece que deu mais força pra ele, que venceu”, conta.</p><br>
                
                <p>Nos anos seguintes, destaque para os Jogos Universitários Brasileiros, disputados em Natal, em 1977. O bom desempenho garantiu a classificação para a Universíade – a olimpíada universitária – do mesmo ano, em Sófia, na Bulgária.</p><br>
                
                <p>“Nunca pensei em ir para a Europa. Só sonhava, pelas aulas de história da escola, em conhecer a Grécia. No fim da competição, eu, meu irmão Ewerson e o José Roberto Matos (também nadador de Mogi) fizemos uma viagem pela Europa e a primeira parada foi exatamente Atenas.” A viagem ainda contou com Roma, Nice, Mônaco, Paris, Londres, Madri e Lisboa.</p><br>
                
                <h3>Fim da carreira</h3>
                
                <p>O ano de 1978 teve duas medalhas de prata no Troféu Brasil (100 e 200 metros borboleta) e deveria marcar a aposentadoria das piscinas. Mas o Clube Náutico Mogiano se candidatou e acabou sendo a sede da competição de 1979.</p><br>
                
                <p>“Eu tinha 24 anos e estava saturado dos treinamentos. A gente nadava de 10 a 12 mil metros por dia. E tinha feito a opção pela educação física, em que havia me formado. Mas não poderia deixar de participar desta competição. Meu rendimento nos treinamentos caiu, mas mesmo assim consegui duas medalhas de bronze: nos 100 metros borboleta e no revezamento”, lembra.</p><br>
                
                <p>Fora das competições, Akcel treinou as categorias de base de natação do Náutico até 1980, quando a modalidade foi encerrada. Depois disso, migrou para o futebol, onde ficou até 1991, quando passou a trabalhar no basquete, primeiro em Suzano, depois em Mogi das Cruzes, em uma equipe que fez história na cidade, conquistando, inclusive o título paulista de 1996.</p><br>
                
                <p>“Na minha época, a natação era o xodó da cidade porque tínhamos vários atletas de Mogi. Quando veio o basquete a relação de identidade com o município foi igual, não só com o título do Campeonato Paulista, mas com os Jogos Abertos em que fomos várias vezes campeões. Foi muito bom participar destes momentos tão importantes na história esportiva da cidade”, finaliza Akcel, que hoje é diretor da Secretaria Municipal de Esporte e Lazer da Prefeitura de Mogi das Cruzes.</p><br>

