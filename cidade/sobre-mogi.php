                    <div class="txt_in scroll-pane">
                        <p>Um município com 451 anos, quase 400 mil habitantes e que une a tradição com o desenvolvimento. Essa é Mogi das Cruzes, cidade localizada no leste da Região Metropolitana de São Paulo e que receberá, entre os dias 7 e 19 de novembro, o mais importante evento esportivo do Estado de São Paulo, os Jogos Abertos do Interior.</p><br>
                        
                        <p>Fundada em 1º de setembro de 1560, Mogi das Cruzes possui uma localização privilegiada, próxima aos dois principais centros econômicos do País, vizinha ao Aeroporto de Cumbica e com fácil acesso ao Litoral e aos Portos de Santos e São Sebastião. Além disso, o município possui a maior área industrial disponível na Grande São Paulo, o Distrito Industrial do Taboão, às margens da Rodovia Ayrton Senna e próximo à Rodovia Presidente Dutra.</p><br>
                        
                        <p>Na área agrícola, Mogi das Cruzes tem destaque na produção hortifruti e de flores, como as orquídeas, além de ser a maior produtora nacional de caqui e nêspera.</p><br>
                        
                        <p>A cidade também possui uma grande diversidade cultural. Em Mogi das Cruzes é realizada a maior e mais antiga Festa do Divino do Brasil, com mais de 400 anos de história e que neste ano acontecerá entre os dias 2 e 12 de Junho. A colônia japonesa também tem uma participação forte na cultura mogiana, com a realização de festas como o Akimatsuri – a Festa do Outono – e o Furusato Matsuri.</p><br>
                        
                        <p>Mogi das Cruzes está de braços abertos para receber atletas, dirigentes e turistas que querem conhecer as suas belezas. </p>
