                    <p>A história dos Jogos Abertos do Interior começou em 1936, quando foi organizada a primeira edição da competição, em Monte Alto, interior de São Paulo. À frente dessa empreitada estavam Horácio “Baby” Barione, ex-jogador de basquete de importantes clubes da capital paulista, e Manuel Carvalho Lima, então presidente da Associação Montealtense.</p><br><br>

                    <p>Com o nome de “Campeonato Aberto do Interior”, a competição reuniu seis cidades que disputaram jogos de basquete. O título ficou com a cidade mineira de Uberlândia, que jogou contra Franca, Mirassol, Monte Alto, Olímpia e Piracicaba.</p>
                </div>
                <div class="txt">
                    <p>Em 1937, os Jogos Abertos aconteceram em Uberlândia. Essa foi a única vez em que a competição aconteceu em uma cidade fora do Estado de São Paulo. Mais uma vez, os mineiros conquistaram o título. Nesse ano, a novidade foi a integração da modalidade natação às disputas.</p><br><br>
                    
                    <p>No ano seguinte, os Jogos Abertos do Interior ganharam o reforço das competições de atletismo. Na competição realizada em Sorocaba, Uberlândia conquista o seu tricampeonato, curiosamente na sua última participação. Com três modalidades, o evento começou a ganhar as suas características atuais e abriu espaço para a entrada de mais modalidades, se transformando na maior festa esportiva do Estado de São Paulo, em que competem todos os anos os maiores atletas do Brasil.</p><br><br>
                    
                    <p>Nesses 75 anos, apenas uma vez os Jogos Abertos não aconteceram. Foi em 1989, quando uma greve de professores da rede estadual de ensino inviabilizou a utilização das escolas estaduais como alojamento. As competições voltaram no ano seguinte, com a disputa na cidade de Araçatuba.</p><br><br>
                    
                    <p>Em 2008, outra novidade importante foi implantada: a separação das cidades em duas divisões. Naquele ano, 12 cidades participaram da 1ª Divisão, enquanto o restante esteve na 2ª Divisão. Desde então, essa estrutura foi mantida, e hoje teremos 14 representantes na 1ª Divisão: a cidade sede, as campeãs das oito regiões esportivas que disputam os Jogos Regionais, a campeã, a vice e a terceira colocada da 1ª Divisão dos Jogos Abertos de 2010 e as duas melhores cidades da 2ª Divisão de 2010.</p><br><br>
                    
                    <p>E assim, com uma história repleta de vitórias, superação e muito esporte, os Jogos Abertos chegam a Mogi das Cruzes para a disputa de sua 75ª edição, que vai reunir mais de 15 mil atletas. Venha conosco fazer parte dessa grande festa!</p>
